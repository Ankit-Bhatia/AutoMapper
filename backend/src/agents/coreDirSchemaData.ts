function pattern(
  xmlField: string,
  sfApiName: string,
  sfObject: string,
  kind: PatternKind,
  notes = '',
): ConfirmedPattern {
  return {
    xmlField,
    sfApiNames: [sfApiName],
    sfObject,
    confidence: kind === 'exact' ? 'HIGH' : 'MEDIUM',
    notes,
    isOneToMany: COREDIR_ONE_TO_MANY_SET.has(normalizeFieldName(xmlField)),
    isFormulaTarget: false,
    isPersonAccountOnly: sfApiName.startsWith('Person') || sfApiName.endsWith('__pc'),
  };
}

