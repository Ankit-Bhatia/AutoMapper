export const COREDIR_FSC_PATTERNS: Record<string, ConfirmedPattern[]> = RAW_COREDIR_PATTERNS.reduce<Record<string, ConfirmedPattern[]>>((acc, entry) => {
  const key = normalizeFieldName(entry.xmlField);
  if (!acc[key]) {
    acc[key] = [];
  }
  acc[key].push(entry);
  return acc;
}, {});
